					Ensuite, pour l'installer  sous Windows XP, ou Windows Seven 32 ou 64 bits, il suffit de le mettre dans le lecteur de CD, ou bien d'exécuter le fichier autorun.bat, (ou encore le fichier Install.exe). <br />
					Le CD installe Java Runtime Environment (JRE 1.6) dans le dossier jre.<br />
					Ensuite, vous pouvez installer et désinstaller sélectivement les projets du CD, la synthèse vocale est installée automatiquement.<br />
					Un raccourci est placé sur le bureau, qui permet de sélectionner le projet à exécuter dans la liste des projets installés. Un autre raccourci permet d'accéder à l'aide sur les projets. Voir également le livret du CD 2011 pour plus d'informations.<br />
