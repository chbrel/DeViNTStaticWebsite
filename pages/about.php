  		<div class="row-fluid">
  			<div class="span8 offset2">
				<p>Depuis huit ans, chaque année, les élèves ingénieurs informaticiens de Polytech Nice Sophia inventent des logiciels destinés aux utilisateurs mal-voyants et non voyants des ordinateurs.</p>
<p>En pratique, ces logiciels sont développés en très grande majorité dans le cadre des projets DeViNT (DeViNT signifie Déficients Visuels et Nouvelles Technologies). Ils sont ensuite distribués gratuitement,  sur les CD DeViNT pour certains, et sur ce site pour tous les autres, puis qu’il en existe maintenant plus de cent, avec leur synthèse vocale VocalyzeSIVOX.</p>
<p>La formule pédagogique des projets DeViNT est originale, et n’a pas d’équivalent à notre connaissance. Elle semble extrêmement formatrice et intéressante pour les élèves ingénieurs, de l’avis de tous.</p>
<p>Dans la forme actuelle, les projets DeViNT durent 12 semaines successives, au second semestre de l’année universitaire, en première année de spécialisation en Sciences Informatiques (troisième année d’école). Au semestre précédent, les élèves ont appris pour certains, et approfondi pour d’autres, le langage java.</p>
<p>
	<ul>
		<li>Ils se constituent en groupes de trois ou quatre membres et proposent un projet, chaque groupe dispose d’un espace pour rendre les productions du projet.</li>
		<li>Ils proposent un sujet, puis un cahier de spécification, qui doivent être validés par les enseignants tuteurs, l’objectif est de réaliser un logiciel accessible et utilisable par un public mal voyant voire non voyant.</li>
		<li>Ils mettent en oeuvre leur logiciel collaborativement en utilisant des outils de gestion de projet tels svn.</li>
		<li>Ils rencontrent des utilisateurs en cours de développement pour collaborer avec eux et ils présentent leur logiciel lors de la journée DeViNT (fin mai) en fin de projet. Selon les années, ils candidatent au prix DeViNT, ou au Challenge Handicap et Technologie à Metz.</li>
	</ul>
</p>
<p>Ces projets enrichissent la formation de nos élèves ingénieurs, pour les raisons suivantes :
	<ul>
		<li>ils permettent d’assimiler le langage java et d’évaluer les outils associés, tels Eclipse</li>
		<li>ils permettent de rencontrer pour la première fois les utilisateurs</li>
		<li>ils introduisent la problématique de l’accessibilité, puisque c’est une contrainte forte des utilisateurs</li>
		<li>ils forment à respecter des contraintes pour l’intégration des projets sur le CD et sa diffusion sur le site</li>
		<li>ils offrent la satisfaction d’inventer et de créer de A à Z avec les technologies enseignées et en cohérence des contraintes fortes, un projet susceptible d’être intégré dans la famille des projets DeViNT.</li>
	</ul>
</p>
